#include <stdio.h>

int main() {
    printf("Hello, World!\n");               // New line
    printf("C\tProgramming\tLanguage\n");   // Horizontal tabs
    printf("Backslash: \\\n");              // Backslash
    printf("Single Quote: \' \n");          // Single quote
    printf("Double Quote: \" \n");          // Double quote
    printf("Carriage Return Test\rCR\n");  // Carriage return
    printf("Backspace Test: ABC\bD\n");    // Backspace
    printf("Alert (Beep Sound): \a\n");    // Alert

    return 0;
}
